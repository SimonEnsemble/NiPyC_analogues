# ╔═╡ 044732fb-8023-4480-a895-d07d87bd1aa7
begin
	temp = 298
	
	data = Dict{Symbol, DataFrame}()
	mof_keys = [:nipyc, :nipycnh]
	# get data files for given temperature
	data_filemanes = ["NiPyC_CO2_298K.csv", "NiPyC-NH2_CO2_298K.csv"]
	# loop over acquired data files	
	for (i, filename) in enumerate(data_filemanes)
		key = mof_keys[i]
		
		# read file
		file = joinpath(pwd(), filename)
		xptl_data = CSV.read(file, DataFrame, header=2)

		rename!(xptl_data, Dict("pressure (bar)" => Symbol("pressure (bar)"), "adsorption (mmol/g)" => Symbol("⟨N⟩ (mmol/g)")))

		pushfirst!(xptl_data[!, Symbol("pressure (bar)")], 0.0)
		pushfirst!(xptl_data[!, Symbol("⟨N⟩ (mmol/g)")], 0.0)

		# Ignore desorption.
		p_max = maximum(xptl_data[!, Symbol("pressure (bar)")])
		des_id = findfirst(xptl_data[!, Symbol("pressure (bar)")] .== p_max)

		# store in a dictionary
		push!(data, key => xptl_data[1:des_id, :])
	end
	data
end

# ╔═╡ 73734eb1-477b-46a8-81de-16be1ee469a9
begin
	Henry = Dict{Symbol, Dict}()
	npts = 3
	
	for key in keys(data)
		# fit henry
	    params = fit_adsorption_isotherm(data[key][1:npts, :], Symbol("pressure (bar)"), Symbol("⟨N⟩ (mmol/g)"), :henry)

		push!(Henry, key => params)
	end
	Henry
end

# ╔═╡ fdfd443e-6de0-4988-80db-95cfe748c770
begin
	gas_to_color = Dict("CO2" => "C2")
	for xtal_name in mof_keys
		figure()
		str = @sprintf("CO₂, H = %0.2f mmol/(g-bar)", Henry[xtal_name]["H"])

		num_pts = npts
		
		scatter(data[xtal_name][1:num_pts, Symbol("pressure (bar)")], 
				data[xtal_name][1:num_pts, Symbol("⟨N⟩ (mmol/g)")],
				label="CO₂ (used for fitting)",
				color="C5",
				marker="o")

		scatter(data[xtal_name][num_pts+1:end, Symbol("pressure (bar)")], 
				data[xtal_name][num_pts+1:end, Symbol("⟨N⟩ (mmol/g)")],
				label="CO₂ (not used or fitting)",
				color="C5",
				marker="o", facecolor="none", lw=1.25)

		x =  range(0, stop=1.0, length=100)
		fit_line = x * Henry[xtal_name]["H"]			
		plot(x, fit_line, color="C5", 
			label=str, linestyle="--")

		
		xlim(xmin=-0.002, xmax=0.2)
		ylim(ymin=-0.015, ymax=4.0)

		legend(title="T=298 K", loc="upper left", fontsize=12)
		xlabel("pressure (bar)")
		ylabel("⟨N⟩ (mmol/g)")
		tight_layout()
		savefig("Henry_fits_$(xtal_name).pdf", dpi=600, format="pdf")
		gcf()	
	end
end

